#accessing list elements
#same like string we can access list elements using indexing and slicing via []
my_list = [1, 2, 3, 'hello', [4, 5]]
print(my_list[0]) #1
print(my_list[3]) #'hello'

#slicing   
#slicing in lists is similar to slicing in strings. It allows you to access a range of elements in the list.
print(my_list[1:4]) #[2, 3, 'hello']
print(my_list[:3]) #[1, 2, 3]   



